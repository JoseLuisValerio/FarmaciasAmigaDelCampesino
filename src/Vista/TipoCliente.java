    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(!Valido.txtVacio(txtNombre) && !Valido.txtVacio(txtDescuento)){
            if(Controlador.InsertaTipoCliente(txtNombre.getText(), txtDescuento.getText())){
                Util.txtLimpiar(txtNombre);
                Util.txtLimpiar(txtDescuento);
                Estilo.lblMensajes(lblAlerta, "Se ha agregado correctamente", 3);
                MostrarTabla();
            }else{
                Estilo.lblMensajes(lblAlerta, "Ha ocurrido un error al agregar", 2);
            }
        }else{
            Estilo.lblMensajes(lblAlerta, "Debe completar los datos solicitados", 2);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void txtDescuentoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDescuentoKeyTyped
        char c = evt.getKeyChar();
        if (Character.isLetter(c)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txtDescuentoKeyTyped

    private void tblTiposMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTiposMousePressed
        int fila = tblTipos.getSelectedRow();
        if (fila >= 0) {
            ID = String.valueOf(tblTipos.getValueAt(fila, 0));
            txtNombre.setText(String.valueOf(tblTipos.getValueAt(fila, 1)));
            txtDescuento.setText(String.valueOf(tblTipos.getValueAt(fila, 2)));
            Util.botonHabilitar(btnUpdate, true);
            Util.botonHabilitar(btnAdd, false);
        }
    }//GEN-LAST:event_tblTiposMousePressed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (Controlador.ActualizarTipoCliente(ID, txtNombre.getText(), txtDescuento.getText())) {
            Estilo.lblMensajes(lblAlerta, "Se ha actualizado correctamente", 3);
            Util.txtLimpiar(txtNombre);
            Util.txtLimpiar(txtDescuento);
            MostrarTabla();
            Util.botonHabilitar(btnUpdate, false);
            Util.botonHabilitar(btnAdd, true);
        } else {
            Estilo.lblMensajes(lblAlerta, "Ha ocurrido un error, por favor verifique.", 2);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        Util.botonHabilitar(btnUpdate, false);
            Util.botonHabilitar(btnAdd, true);
            Util.txtLimpiar(txtNombre);
            Util.txtLimpiar(txtDescuento);
            MostrarTabla();
            ID="";
    }//GEN-LAST:event_btnCancelActionPerformed

